// If the number in the decimal point is less than 0.5 then the number will not increase
// If the number in the decimal point is more or equal to 0.5 then the number will increase by 1
console.log(Math.round(4.25)); // Output: 4
console.log(Math.round(4.50)); // Output: 5
console.log(Math.round(4.99)); // Output: 5